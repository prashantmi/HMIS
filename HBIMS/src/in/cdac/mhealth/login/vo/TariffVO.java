package in.cdac.mhealth.login.vo;

public class TariffVO {
	
	private String patientCategory;
	private String chargeType;
	private String tariffName;
	private String tariffCharge;
	private String tariffServiceID;
	
	public String getPatientCategory() {
		return patientCategory;
	}
	public void setPatientCategory(String patientCategory) {
		this.patientCategory = patientCategory;
	}
	public String getChargeType() {
		return chargeType;
	}
	public void setChargeType(String chargeType) {
		this.chargeType = chargeType;
	}
	public String getTariffName() {
		return tariffName;
	}
	public void setTariffName(String tariffName) {
		this.tariffName = tariffName;
	}
	public String getTariffCharge() {
		return tariffCharge;
	}
	public void setTariffCharge(String tariffCharge) {
		this.tariffCharge = tariffCharge;
	}
	public String getTariffServiceID() {
		return tariffServiceID;
	}
	public void setTariffServiceID(String tariffServiceID) {
		this.tariffServiceID = tariffServiceID;
	}
	
	
	

}

