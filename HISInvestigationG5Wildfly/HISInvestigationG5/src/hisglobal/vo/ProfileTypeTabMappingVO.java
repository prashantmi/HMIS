package hisglobal.vo;

public class ProfileTypeTabMappingVO extends ValueObject
{
	private String profileType;
	private String profileName;
	private String deskMenuId;
	private String deskMenuName;
	private String profileTabOrder;
	
	
	public String getProfileType() {
		return profileType;
	}
	public void setProfileType(String profileType) {
		this.profileType = profileType;
	}
	public String getProfileName() {
		return profileName;
	}
	public void setProfileName(String profileName) {
		this.profileName = profileName;
	}
	public String getDeskMenuId() {
		return deskMenuId;
	}
	public void setDeskMenuId(String deskMenuId) {
		this.deskMenuId = deskMenuId;
	}
	public String getDeskMenuName() {
		return deskMenuName;
	}
	public void setDeskMenuName(String deskMenuName) {
		this.deskMenuName = deskMenuName;
	}
	public String getProfileTabOrder() {
		return profileTabOrder;
	}
	public void setProfileTabOrder(String profileTabOrder) {
		this.profileTabOrder = profileTabOrder;
	}
	
	
	

}
